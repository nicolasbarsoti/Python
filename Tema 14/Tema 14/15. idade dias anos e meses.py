# Entrada de dados do usuario
anos = int(input("Digite a idade em anos: "))

# Cálculo da idade em dias
dias = anos * 365
#calculo idade em meses
meses = anos * 12
# Saída do resultado
print("Sua idade em anos é:", anos)
print("A idade em meses é:", meses)
print("A idade em dias é:", dias)
